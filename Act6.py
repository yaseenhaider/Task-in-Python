List = ['Y', 'A', 'S','E', 'E', 'N','H', 'A', 'I', 'D', 'E', 'R']
print("Initial List: ")
print(List)

Sliced_List = List[3:8]
print("\nSlicing elements in a range 3-8: ")
print(Sliced_List)

Sliced_List = List[5:]
print("\nElements sliced from 5th element till the end: ")
print(Sliced_List)

Sliced_List = List[:]
print("\nPrinting all elements using slice operation: ")
print(Sliced_List)